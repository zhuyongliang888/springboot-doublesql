package com.hoperun.oracle.demo.model;

public class User {
    private Short idp;

    private Short age;

    private Short height;

    public Short getIdp() {
        return idp;
    }

    public void setIdp(Short idp) {
        this.idp = idp;
    }

    public Short getAge() {
        return age;
    }

    public void setAge(Short age) {
        this.age = age;
    }

    public Short getHeight() {
        return height;
    }

    public void setHeight(Short height) {
        this.height = height;
    }
}